package com.newproj.report.school.dto;

public class SchoolConnectorParam {
	private Integer id ;
	private String masterName ;
	private String masterPhone ;
	private String viceName ;
	private String vicePhone ;
	private String reportName ;
	private String reportPhone ;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getMasterName() {
		return masterName;
	}
	public void setMasterName(String masterName) {
		this.masterName = masterName;
	}
	public String getMasterPhone() {
		return masterPhone;
	}
	public void setMasterPhone(String masterPhone) {
		this.masterPhone = masterPhone;
	}
	public String getViceName() {
		return viceName;
	}
	public void setViceName(String viceName) {
		this.viceName = viceName;
	}
	public String getVicePhone() {
		return vicePhone;
	}
	public void setVicePhone(String vicePhone) {
		this.vicePhone = vicePhone;
	}
	public String getReportName() {
		return reportName;
	}
	public void setReportName(String reportName) {
		this.reportName = reportName;
	}
	public String getReportPhone() {
		return reportPhone;
	}
	public void setReportPhone(String reportPhone) {
		this.reportPhone = reportPhone;
	}
}
